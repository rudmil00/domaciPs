    private void prepareView() {

        List<Zvanje> zvanja = Controller.getInstance().getAllZvanja();
        for (Zvanje zvanje : zvanja) {
            cbZvanje.addItem(zvanje);
        }

        switch (view) {
            case 1:
                prepareAdd();
                break;
            case 2://update
                break;
            case 3:
                view();
                break;//view
        }

    }

    private void view() {
        textInputIme.setEditable(false);
        textInputPrezime.setEditable(false);
        cbZvanje.setEnabled(false);
        btnAdd.setVisible(false);
        btnChange.setVisible(true);

        textInputIme.setText(n.getIme());
        textInputPrezime.setText(n.getPrezime());
        cbZvanje.setSelectedItem(n.getZvanje());
    }

    private void prepareAdd() {
        btnDelete.setVisible(false);
        btnChange.setVisible(false);
    }
