    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
            String ime = textInputIme.getText();
            String prezime = textInputPrezime.getText();
            Zvanje zvanje = (Zvanje) cbZvanje.getSelectedItem();
            Nastavnik n = new Nastavnik(ime, prezime, zvanje);
            if (n.isValid()) {
                Controller.getInstance().insertUpdate(n, false);
            } else {
                JOptionPane.showMessageDialog(this, "Nevalidni unos", "Greska", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException ex) {
            Logger.getLogger(FrmNastavnik.class.getName()).log(Level.SEVERE, null, ex);
        }

        this.dispose();

    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed

        int a = JOptionPane.showConfirmDialog(null, "Da li zelite da obriste nastavnika", "Delete", JOptionPane.YES_NO_OPTION);
        if (a == JOptionPane.YES_OPTION) {
            try {
                Controller.getInstance().removeNastavnik(n.getId());
                System.out.println("Usespno obrisan nastavnik sa id" + n.getId());
               this.setVisible(false);
            } catch (SQLException ex) {
                Logger.getLogger(FrmNastavnik.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            
        }


    }//GEN-LAST:event_btnDeleteActionPerformed

